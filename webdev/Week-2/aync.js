//asynchronous javascript

//what is synchronous programming?
//synchronous programming is a programming paradigm in which the code is executed sequentially,
//  meaning that each operation must complete before the next one begins. 
// In synchronous programming, the program waits for each operation to finish before moving on to the next one.
//  This can lead to blocking and delays if an operation takes a long time to complete,
//  such as fetching data from a server or reading a file from disk.

//what is asynchronous programming?
//Asynchronous programming is a programming paradigm in which the code can be executed out of order,
// allowing for non-blocking operations. In asynchronous programming, the program can continue
//  to execute other code while waiting for an operation to complete.
//  This is often achieved through the use of callbacks, promises, or async/await syntax. 
// Asynchronous programming is particularly useful for tasks that involve I/O operations, such as fetching data
//  from a server or reading/writing files, as it allows the program to remain responsive and efficient.



//why we need asynchronous programming?
//Asynchronous programming is needed to improve the performance and responsiveness of applications, 
// especially when dealing with tasks that involve I/O operations or long-running processes. 
// Here are some reasons why asynchronous programming is important:
//1. Non-blocking operations: Asynchronous programming allows the program to continue
//  executing other code while waiting for an operation to complete, preventing blocking and 
// improving responsiveness.
//2. Improved performance: Asynchronous programming can help improve the performance
//  of applications by allowing multiple operations to run concurrently,
//  rather than waiting for each operation to finish before starting the next one.
//3. Better user experience: Asynchronous programming can help create a 
// better user experience by keeping the application responsive and 
// preventing it from freezing or becoming unresponsive during long-running operations.
//4. Scalability: Asynchronous programming can help improve the scalability 
// of applications by allowing them to handle more concurrent requests without blocking or slowing down.

//async exmaple
console.log("start");

setTimeout(() => {
    console.log("This is an asynchronous operation");
}, 2000);

console.log("end");
//using async and await
async function fetchData() {
    try {
        let response = await fetch('https://jsonplaceholder.typicode.com/posts/1');
        let data = await response.json();
        console.log(data);
    } catch (error) {
        console.error('Error fetching data:', error);
    }

}

fetchData();

//callback example
function fetchDataWithCallback(callback) {
    setTimeout(() => {
        const data = { id: 1, title: 'Sample Data' };
        callback(data);
    }, 2000);
}
fetchDataWithCallback((data) => {
    console.log('Data received:', data);
}
);
//promise example
function fetchDataWithPromise() {
    return new Promise((resolve, reject) => {
        setTimeout(() => {
            const data = { id: 1, title: 'Sample Data' };
            resolve(data);
        }
        , 2000);
    }
);
}
fetchDataWithPromise()
    .then((data) => {
        console.log('Data received:', data);
    })
    .catch((error) => {
        console.error('Error fetching data:', error);
    });













































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































